class FailureDetector:
    def __init__(self, logger, health_monitor):
        self.logger = logger
        self.hm     = health_monitor

    def classify(self, exception=None) -> FailureType:
        """Map exception or health monitor signal to FailureType."""
        # Health monitor already classified it
        if self.hm.failure_event.is_set():
            return self.hm.failure_type

        if exception is None:
            return FailureType.UNKNOWN

        msg = str(exception).lower()
        if "collision" in msg:
            return FailureType.COLLISION
        if "estop" in msg or "emergency" in msg:
            return FailureType.ESTOP
        if "socket" in msg or "connection" in msg or "timeout" in msg:
            return FailureType.NETWORK_DOWN
        if "ik" in msg or "unreachable" in msg or "no solution" in msg:
            return FailureType.IK_FAILURE
        if "singularity" in msg or "singular" in msg:
            return FailureType.SINGULARITY
        return FailureType.PROGRAM_CRASH

# ============================================================
# RECOVERY MANAGER — policy per failure type
# ============================================================

class RecoveryManager:
    def __init__(self, logger, health_monitor, recovery_path_mgr,
                 vehicle_validator, RDK, robot):
        self.logger    = logger
        self.hm        = health_monitor
        self.rpm       = recovery_path_mgr
        self.validator = vehicle_validator
        self.RDK       = RDK
        self.robot     = robot
        self._network_retry_count = 0

    def recover(self, failure_type: FailureType, pose_index: int) -> str:
        """
        Returns: 'resume' | 'restart' | 'abort' | 'manual'
        """
        self.logger.info("RECOVERY_START",
                         failure=failure_type.name, pose_index=pose_index)

        if failure_type == FailureType.COLLISION:
            return self._recover_collision(pose_index)

        elif failure_type == FailureType.ESTOP:
            return self._recover_estop(pose_index)

        elif failure_type in (FailureType.POWER_LOSS, FailureType.PROGRAM_CRASH):
            return self._recover_power_loss(pose_index)

        elif failure_type == FailureType.NETWORK_DOWN:
            return self._recover_network(pose_index)

        elif failure_type == FailureType.ROBOT_STUCK:
            return self._recover_stuck(pose_index)

        elif failure_type == FailureType.IK_FAILURE:
            return self._recover_ik_failure(pose_index)

        elif failure_type == FailureType.SINGULARITY:
            return self._recover_singularity(pose_index)

        elif failure_type == FailureType.VEHICLE_SHIFTED:
            return self._recover_vehicle_shifted(pose_index)

        elif failure_type == FailureType.VARIANT_CHANGED:
            return self._recover_variant_changed(pose_index)

        else:
            self.logger.warn("UNKNOWN_FAILURE_TYPE", fallback="abort")
            return self._abort()

    # ── Per-failure policies ──────────────────────────────────────────────────

    def _recover_collision(self, pose_index):
        """Local Z offset → retreat → revalidate → resume"""
        self.logger.info("POLICY_COLLISION")
        offset_ok = self.rpm.local_safe_offset(z_offset_mm=60.0)
        if not offset_ok:
            self.logger.error("OFFSET_FAILED_GOING_MANUAL")
            return "manual"
        retreat_ok = self.rpm.retreat(pose_index)
        if not retreat_ok:
            return "manual"
        if self.validator.revalidate_mid_inspection():
            self.logger.info("COLLISION_RECOVERY_OK", action="resume")
            return "resume"
        return self._abort()

    def _recover_estop(self, pose_index):
        self.logger.warn("POLICY_ESTOP")
        retreat_ok = self.rpm.retreat(pose_index)
        if not retreat_ok:
            return "manual"
        self.logger.info("ESTOP_RECOVERY_OK", action="resume")
        return "resume"

    def _recover_power_loss(self, pose_index):
        self.logger.info("POLICY_PROGRAM_CRASH_RECOVERY")
        retreat_ok = self.rpm.retreat(pose_index)
        if not retreat_ok:
            return "manual"
        if self.validator.revalidate_mid_inspection():
            self.logger.info("PROGRAM_CRASH_RECOVERY_OK", action="restart")
            return "restart"
        return self._abort()

    def _recover_network(self, pose_index):
        """Retry RoboDK connection N times → resume or abort"""
        self.logger.warn("POLICY_NETWORK_RETRY")
        for attempt in range(1, NETWORK_RETRIES + 1):
            self.logger.info("NETWORK_RETRY", attempt=attempt)
            time.sleep(NETWORK_RETRY_DELAY)
            try:
                self.RDK.Connect()
                self.logger.info("NETWORK_RECONNECTED", attempt=attempt)
                self._network_retry_count = 0
                return "resume"
            except Exception as e:
                self.logger.error("NETWORK_RETRY_FAILED",
                                  attempt=attempt, error=str(e))
        self.logger.error("NETWORK_EXHAUSTED_RETRIES")
        return self._abort()

    def _recover_stuck(self, pose_index):
        """Watchdog timeout → local offset → retreat → resume"""
        self.logger.info("POLICY_ROBOT_STUCK")
        self.robot.Stop()
        time.sleep(1.0)
        offset_ok = self.rpm.local_safe_offset(z_offset_mm=50.0)
        if not offset_ok:
            return "manual"
        retreat_ok = self.rpm.retreat(pose_index)
        if not retreat_ok:
            return "manual"
        self.logger.info("STUCK_RECOVERY_OK", action="resume")
        return "resume"

    def _recover_ik_failure(self, pose_index):
        """IK failed at pose → skip if non-critical → retreat → resume"""
        self.logger.warn("POLICY_IK_FAILURE", pose_index=pose_index)
        pose_name = INSPECTION_SEQUENCE[pose_index]
        # Non-critical: poses that are supplemental (8a, 11a, 15–18)
        NON_CRITICAL = {"Target 8a", "Target 11a", "Target 15",
                        "Target 16", "Target 17", "Target 18"}
        if pose_name in NON_CRITICAL:
            self.logger.warn("IK_SKIP_NON_CRITICAL", pose=pose_name)
            return "resume"  # caller increments index
        retreat_ok = self.rpm.retreat(pose_index)
        if not retreat_ok:
            return "manual"
        return self._abort()

    def _recover_singularity(self, pose_index):
        """Pre-detected singularity → retreat → abort (no blind retry)"""
        self.logger.error("POLICY_SINGULARITY", pose_index=pose_index)
        self.rpm.retreat(pose_index)
        return self._abort()

    def _recover_vehicle_shifted(self, pose_index):
        """Pause → revalidate ROI → resume if within tolerance else abort"""
        self.logger.warn("POLICY_VEHICLE_SHIFTED")
        retreat_ok = self.rpm.retreat(pose_index)
        if not retreat_ok:
            return "manual"
        if self.validator.validate():
            self.logger.info("VEHICLE_REVALIDATED_OK", action="restart")
            return "restart"
        self.logger.error("VEHICLE_OUT_OF_TOLERANCE")
        return self._abort()

    def _recover_variant_changed(self, pose_index):
        """Variant mismatch → attempt config swap → abort if unknown"""
        self.logger.warn("POLICY_VARIANT_CHANGED")
        # In simulation: try each known variant
        for variant_name in VARIANTS:
            if self.validator.validate(variant=variant_name):
                self.logger.info("VARIANT_MATCHED", variant=variant_name,
                                 action="restart")
                return "restart"
        self.logger.error("VARIANT_UNKNOWN_ABORT")
        self.rpm.retreat(pose_index)
        return self._abort()

    def _abort(self):
        self.logger.error("RECOVERY_DECISION", action="ABORT")
        return "abort"

