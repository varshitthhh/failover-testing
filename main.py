class RecoveryPathManager:
    def __init__(self, logger, RDK, robot):
        self.logger = logger
        self.RDK    = RDK
        self.robot  = robot

    def _get_anchor_at_or_before(self, pose_index):
        """Find nearest recovery anchor <= current pose index."""
        anchors = sorted(RECOVERY_WAYPOINTS.keys(), reverse=True)
        for a in anchors:
            if a <= pose_index:
                return a
        return 0

    def retreat(self, from_pose_index):
        """Step back through execution_path in reverse, one pose at a time."""
        execution_path = load_execution_path()
        self.logger.info("RETREAT_START", from_index=from_pose_index)

        for idx in range(from_pose_index - 1, -1, -1):
            step = execution_path[idx]
            self.logger.info("RETREAT_STEP", index=idx, pose=step["name"])
            try:
                if step["type"] == "iw":
                    self.robot.MoveJ(step["joints"])
                else:
                    target = self.RDK.Item(step["name"], ITEM_TYPE_TARGET)
                    if not target.Valid():
                        self.logger.error("RETREAT_TARGET_INVALID", name=step["name"])
                        return False
                    self.robot.MoveJ(target)
                self.logger.info("RETREAT_STEP_OK", index=idx, pose=step["name"])
                time.sleep(0.2)
            except Exception as e:
                self.logger.error("RETREAT_STEP_FAILED", index=idx, pose=step["name"], error=str(e))
                return False

        self.logger.info("RETREAT_COMPLETE", final="Target 1")
        return True

    def _move_joints(self, joints, label=""):
        """Move to a raw joint config (for IW_ intermediates)."""
        try:
            self.robot.MoveJ(joints)
            self.logger.info("RETREAT_IW_STEP_OK", label=label,
                             joints=[round(j,2) for j in joints])
            return True
        except Exception as e:
            self.logger.error("RETREAT_IW_STEP_FAILED", label=label, error=str(e))
            return False

    def local_safe_offset(self, z_offset_mm=50.0):
        """
        Move TCP straight up by z_offset_mm before retreating.
        Used for collision / stuck scenarios.
        """
        try:
            current_pose = self.robot.Pose()
            xyzrpw = list(pose_2_xyzrpw(current_pose))
            xyzrpw[2] += z_offset_mm
            from robodk.robomath import xyzrpw_2_pose
            offset_pose = xyzrpw_2_pose(xyzrpw)
            self.robot.MoveL(offset_pose)
            self.logger.info("LOCAL_OFFSET_APPLIED", z_offset_mm=z_offset_mm)
            return True
        except Exception as e:
            self.logger.error("LOCAL_OFFSET_FAILED", error=str(e))
            return False

    def _move_to_target(self, target_name):
        try:
            target = self.RDK.Item(target_name, ITEM_TYPE_TARGET)
            if not target.Valid():
                self.logger.error("TARGET_NOT_FOUND", name=target_name)
                return False
            self.robot.MoveJ(target)
            return True
        except Exception as e:
            self.logger.error("MOVE_FAILED", target=target_name, error=str(e))
            return False

