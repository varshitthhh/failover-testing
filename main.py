class StructuredLogger:
    def __init__(self, log_file=LOG_FILE):
        self.log_file = log_file
        self.entries = []
        logging.basicConfig(
            level=logging.INFO,
            format="[%(asctime)s] %(levelname)s %(message)s",
            datefmt="%H:%M:%S"
        )
        self.console = logging.getLogger("BIW")

    def log(self, level, event, **kwargs):
        entry = {
            "ts": datetime.now().isoformat(),
            "level": level,
            "event": event,
            **kwargs
        }
        self.entries.append(entry)
        msg = f"{event}" + (f" | {kwargs}" if kwargs else "")
        if level == "ERROR":
            self.console.error(msg)
        elif level == "WARN":
            self.console.warning(msg)
        else:
            self.console.info(msg)
        # Append to file immediately (survive crash)
        with open(self.log_file, "a") as f:
            f.write(json.dumps(entry) + "\n")

    def info(self, event, **kw):  self.log("INFO",  event, **kw)
    def warn(self, event, **kw):  self.log("WARN",  event, **kw)
    def error(self, event, **kw): self.log("ERROR", event, **kw)

