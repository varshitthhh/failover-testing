class InspectionExecutor:
    def __init__(self, logger, checkpoint_mgr, health_monitor,
                 failure_detector, recovery_manager, recovery_path_mgr,
                 vehicle_validator, RDK, robot):
        self.logger      = logger
        self.ckpt        = checkpoint_mgr
        self.hm          = health_monitor
        self.fd          = failure_detector
        self.rm          = recovery_manager
        self.rpm         = recovery_path_mgr
        self.validator   = vehicle_validator
        self.RDK         = RDK
        self.robot       = robot
        self.state       = State.IDLE
        self.start_index = 0
        self.failure_index = -1

    def _set_state(self, s: State):
        self.logger.info("STATE_CHANGE",
                         old=self.state.name, new=s.name)
        self.state = s
    
    def _check_signal_file(self):
        """Poll failure_signal.json each pose. Returns (FailureType, subfailure_int) or (None, None)."""
        path = "failure_signal.json"
        if not os.path.exists(path):
            return None, None
        try:
            with open(path) as f:
                sig = json.load(f)
            os.remove(path)  # consume immediately — one-shot
            ftype = FailureType[sig.get("type", "UNKNOWN")]
            sub   = sig.get("subfailure", 1)
            self.logger.warn("SIGNAL_FILE_DETECTED", type=ftype.name, subfailure=sub)
            return ftype, sub
        except Exception as e:
            self.logger.error("SIGNAL_FILE_READ_ERROR", error=str(e))
            return None, None

    def _move(self, target_name):
        target = self.RDK.Item(target_name, ITEM_TYPE_TARGET)
        if not target.Valid():
            raise RuntimeError(f"IK no solution: {target_name}")
        self.hm.notify_move_start()
        self.robot.MoveJ(target)
        self.hm.notify_move_done()
    
    def _move_step(self, step):
        """Move to a named target or raw joint IW_ config."""
        if step["type"] == "iw":
            # IW_ intermediates: move by joint config directly
            self.hm.notify_move_start()
            self.robot.MoveJ(step["joints"])
            self.hm.notify_move_done()
        else:
            # Named target: use existing _move logic
            self._move(step["name"])

    def _pre_singularity_check(self, pose_name, joints):
        """Warn if J5 near 0 (wrist singularity)."""
        if len(joints) >= 5 and abs(joints[4]) < 5.0:
            self.logger.warn("SINGULARITY_PRE_CHECK",
                             pose=pose_name, J5=joints[4])
            return True
        return False

    def run(self):
        # ── State: IDLE → VALIDATING ─────────────────────────────────────────
        self._set_state(State.VALIDATING)
        print("\n" + "="*50)
        print("  BIW Inspection — Failure Recovery Framework")
        print("="*50)

        # Check for cold-restart checkpoint
        ckpt = self.ckpt.load()
        if ckpt:
            print(f"\n[RECOVERY] Cold restart detected.")
            print(f"  Last checkpoint: {ckpt['pose_name']} (index {ckpt['pose_index']})")
            ans = input("  Resume from checkpoint? (y/n): ").strip().lower()
            if ans == "y":
                self.start_index = ckpt["pose_index"]
                self.hm.signal_failure(FailureType.POWER_LOSS)
                # Run power loss recovery inline
                action = self.rm.recover(FailureType.POWER_LOSS, self.start_index)
                if action == "abort":
                    self._set_state(State.ABORTED)
                    return
                if action == "restart":
                    self.start_index = 0
                    self.ckpt.clear()
                self.hm.clear()
            else:
                self.ckpt.clear()

        # Vehicle validation menu
        print("\nIs the vehicle in position and valid?")
        ans = input("  Enter 'yes' to proceed, 'no' to abort: ").strip().lower()
        if ans != "yes":
            print("[ABORT] Vehicle not valid. Exiting.")
            self._set_state(State.ABORTED)
            return

        if not self.validator.validate():
            print("[ABORT] Vehicle failed ROI validation.")
            self.logger.error("VEHICLE_INVALID_ABORT")
            self._set_state(State.ABORTED)
            return

        # ── State: INITIALIZING ──────────────────────────────────────────────
        self._set_state(State.INITIALIZING)
        self.hm.start()
        self.logger.info("INSPECTION_START",
                         total_poses=len(INSPECTION_SEQUENCE),
                         start_index=self.start_index)

        # ── State: INSPECTING ────────────────────────────────────────────────
        self._set_state(State.INSPECTING)
        execution_path = load_execution_path()
        idx = self.start_index
        try:
            while idx < len(execution_path):
                step = execution_path[idx]
                pose_name = step["name"]

                # Poll signal file (injected failures from inject_*.py scripts)
                sig_type, sig_sub = self._check_signal_file()
                if sig_type:
                    self.failure_index = idx  # ← Store the pose index where signal was detected
                    self.hm.signal_failure(sig_type)
                    self.hm._subfailure = sig_sub  # carry subfailure number into handler

                # Check health monitor for async failures
                if self.hm.failure_event.is_set():
                    ftype = self.hm.failure_type
                    # Use stored failure_index, not current idx
                    failure_idx = getattr(self, 'failure_index', idx)
                    self.logger.error("ASYNC_FAILURE_DETECTED",
                                    type=ftype.name, at_pose=pose_name, failure_index=failure_idx)
                    action = self._handle_failure(ftype, failure_idx)
                    if action == "abort":
                        self._set_state(State.ABORTED)
                        return
                    elif action == "manual":
                        self._set_state(State.MANUAL_OVERRIDE)
                        return
                    elif action == "restart":
                        # ── OPTION A: STOP AT HOME, NO RE-INSPECTION ──
                        self.logger.info("RECOVERY_COMPLETE_STOPPED_AT_HOME")
                        self._set_state(State.COMPLETE)
                        self.ckpt.clear()
                        self.hm.stop()
                        self.failure_index = -1
                        return  # ← Exit completely, no re-inspection
                    # resume: continue from current idx
                    self.hm.clear()
                    self.failure_index = -1

                self.logger.info("MOVING_TO", pose=pose_name, index=idx)
                # ── TESTING: Add delay at injection points ──
                if idx in [9, 14, 16, 23]:  # Target 8a, 11, 13, 11a
                    print(f"  [SLOW] Pausing at {pose_name} for 10 seconds...")
                    print(f"  [SLOW] Run inject.py in Terminal 2 NOW!")
                    time.sleep(10.0)
                # ── END TESTING ──
                try:
                    self._move_step(step)
                    # Save checkpoint after every successful move
                    joints = self.robot.Joints().list()
                    self.ckpt.save(idx, pose_name, joints)
                    self.logger.info("POSE_COMPLETE", pose=pose_name, index=idx)

                except KeyboardInterrupt:
                    self.logger.error("KEYBOARD_INTERRUPT", at_pose=pose_name)
                    action = self._handle_failure(FailureType.ESTOP, idx)
                    if action != "resume":
                        self._set_state(State.ABORTED)
                        return
                    self.hm.clear()
                    continue

                except Exception as e:
                    ftype = self.fd.classify(exception=e)
                    self.logger.error("MOVE_EXCEPTION",
                                    pose=pose_name, error=str(e), type=ftype.name)
                    action = self._handle_failure(ftype, idx)
                    if action == "abort":
                        self._set_state(State.ABORTED)
                        return
                    elif action == "manual":
                        self._set_state(State.MANUAL_OVERRIDE)
                        return
                    elif action == "restart":
                        idx = 0
                        continue
                    elif action == "resume":
                        idx += 1  # skip failed pose if non-critical
                        self.hm.clear()
                        continue

                idx += 1
        except Exception as e:
            ftype = self.fd.classify(exception=e)
            self.logger.error("UNHANDLED_EXCEPTION", error=str(e), type=ftype.name)
            self.rpm.retreat(idx)
            self._set_state(State.ABORTED)
            self.hm.stop()
            return
        
        # ── Complete ─────────────────────────────────────────────────────────
        self._set_state(State.COMPLETE)
        self.ckpt.clear()
        self.hm.stop()
        self.logger.info("INSPECTION_COMPLETE")
        print("\n[DONE] Inspection complete. All poses executed successfully.")

    def _handle_failure(self, ftype, idx):
        self._set_state(State.FAILURE)
        self._set_state(State.RECOVERING)
        action = self.rm.recover(ftype, idx)
        if action in ("resume", "restart"):
            self._set_state(State.REVALIDATING)
            if not self.validator.revalidate_mid_inspection():
                self.logger.error("REVALIDATION_FAILED_AFTER_RECOVERY")
                return "abort"
            self._set_state(State.RESUMING)
        return action

