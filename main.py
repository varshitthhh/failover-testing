# HEALTH MONITOR (watchdog thread)
# ============================================================

class HealthMonitor:
    def __init__(self, logger):
        self.logger = logger
        self.failure_event = threading.Event()
        self.failure_type  = FailureType.NONE
        self._stop         = threading.Event()
        self._last_pose_time = time.time()
        self._robot_moving = False
        self._thread = threading.Thread(target=self._run, daemon=True)

    def start(self):
        self._thread.start()
        self.logger.info("HEALTH_MONITOR_STARTED")

    def stop(self):
        self._stop.set()

    def notify_move_start(self):
        self._robot_moving = True
        self._last_pose_time = time.time()

    def notify_move_done(self):
        self._robot_moving = False
        self._last_pose_time = time.time()
        self._write_heartbeat()

    def _write_heartbeat(self):
        with open(HEARTBEAT_FILE, "w") as f:
            f.write(datetime.now().isoformat())

    def _check_heartbeat_age(self):
        """Detect power loss on cold restart — heartbeat file too old."""
        if not os.path.exists(HEARTBEAT_FILE):
            return False
        age = time.time() - os.path.getmtime(HEARTBEAT_FILE)
        return age > 30.0  # 30s stale = likely power loss

    def _run(self):
        while not self._stop.is_set():
            time.sleep(1.0)
            if self.failure_event.is_set():
                continue
            # Stuck detection
            if self._robot_moving:
                elapsed = time.time() - self._last_pose_time
                if elapsed > WATCHDOG_TIMEOUT:
                    self.logger.error("WATCHDOG_STUCK_DETECTED",
                                      elapsed=round(elapsed, 1))
                    self.failure_type = FailureType.ROBOT_STUCK
                    self.failure_event.set()

    def signal_failure(self, ftype: FailureType):
        self.failure_type = ftype
        self.failure_event.set()
        self.logger.error("FAILURE_SIGNALED", type=ftype.name)

    def clear(self):
        self.failure_event.clear()
        self.failure_type = FailureType.NONE
        self._subfailure = 1  # subfailure index from inject script

