class VehicleValidator:
    def __init__(self, logger, RDK):
        self.logger = logger
        self.RDK = RDK

    def validate(self, variant="hyryder_standard"):
        """
        Production: replace with camera bbox or PLC signal.
        Simulation: check RoboDK object pose within ROI bounds.
        """
        try:
            vehicle = self.RDK.Item("Hyryder chasis_extra studs")
            pose = vehicle.Pose()
            xyzrpw = pose_2_xyzrpw(pose)
            x, y, z = xyzrpw[0], xyzrpw[1], xyzrpw[2]
            in_roi = (
                ROI_BOUNDS["x"][0] <= x <= ROI_BOUNDS["x"][1] and
                ROI_BOUNDS["y"][0] <= y <= ROI_BOUNDS["y"][1] and
                ROI_BOUNDS["z"][0] <= z <= ROI_BOUNDS["z"][1]
            )
            tol = VARIANTS.get(variant, VARIANTS["hyryder_standard"])
            # Nominal position for standard Hyryder
            nominal = {"x": 0.0, "y": -1.4, "z": -0.5}
            within_tolerance = (
                abs(x - nominal["x"]) <= tol["x_tol"] and
                abs(y - nominal["y"]) <= tol["y_tol"] and
                abs(z - nominal["z"]) <= tol["z_tol"]
            )
            self.logger.info("VEHICLE_VALIDATION",
                             xyz=(round(x,2), round(y,2), round(z,2)),
                             in_roi=in_roi, within_tolerance=within_tolerance,
                             variant=variant)
            return in_roi and within_tolerance
        except Exception as e:
            self.logger.error("VEHICLE_VALIDATION_ERROR", error=str(e))
            return False

    def revalidate_mid_inspection(self):
        """Called during recovery to check if vehicle shifted."""
        return self.validate()

