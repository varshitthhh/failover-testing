class CheckpointManager:
    def __init__(self, logger):
        self.logger = logger

    def save(self, pose_index, pose_name, joints):
        tmp = CHECKPOINT_FILE + ".tmp"
        data = {
            "pose_index": pose_index,
            "pose_name":  pose_name,
            "joints_deg": joints,
            "timestamp":  datetime.now().isoformat(),
            "state":      "INSPECTING"
        }
        with open(tmp, "w") as f:
            json.dump(data, f, indent=2)
        os.replace(tmp, CHECKPOINT_FILE)  # atomic write
        self.logger.info("CHECKPOINT_SAVED", pose=pose_name, index=pose_index)

    def load(self):
        if not os.path.exists(CHECKPOINT_FILE):
            return None
        try:
            with open(CHECKPOINT_FILE) as f:
                data = json.load(f)
            self.logger.info("CHECKPOINT_LOADED", data=data)
            return data
        except Exception as e:
            self.logger.error("CHECKPOINT_CORRUPT", error=str(e))
            return None

    def clear(self):
        if os.path.exists(CHECKPOINT_FILE):
            os.remove(CHECKPOINT_FILE)

