def main():
    # Connect
    RDK = Robolink()
    if not RDK.Connect():
        print("[ERROR] Cannot connect to RoboDK.")
        sys.exit(1)

    robots = RDK.ItemList(ITEM_TYPE_ROBOT)
    if not robots:
        print("[ERROR] No robot found in station.")
        sys.exit(1)
    robot = robots[0]
    robot.setSpeed(MOVE_SPEED_MM_S)

    # Wire modules
    logger    = StructuredLogger()
    ckpt_mgr  = CheckpointManager(logger)
    hm        = HealthMonitor(logger)
    validator = VehicleValidator(logger, RDK)
    rpm       = RecoveryPathManager(logger, RDK, robot)
    fd        = FailureDetector(logger, hm)
    rm        = RecoveryManager(logger, hm, rpm, validator, RDK, robot)
    executor  = InspectionExecutor(
        logger, ckpt_mgr, hm, fd, rm, rpm, validator, RDK, robot
    )

    # Graceful SIGINT → ESTOP policy
    def sigint_handler(sig, frame):
        logger.error("SIGINT_RECEIVED")
        hm.signal_failure(FailureType.ESTOP)
    signal.signal(signal.SIGINT, sigint_handler)

    executor.run()

    # Print final state
    print(f"\n[STATE] Final: {executor.state.name}")
    print(f"[LOG]   Written to {LOG_FILE}")

if __name__ == "__main__":
    main()